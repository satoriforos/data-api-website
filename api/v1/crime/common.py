
def get_is_eligible(subscription_plan, header):
    pricing_eligibility = {
        "free": [
            "2008",
        ],
        "basic": [
            "2008", "totals"
        ],
        "standard":  [str(year) for year in range(1981, 2009)] + ["totals"],
        "business":  [str(year) for year in range(1981, 2009)] + ["totals"],
    }
    is_eligible = False
    if subscription_plan is not None:
        if subscription_plan.name in pricing_eligibility:
            year = header[-4:]
            if year in pricing_eligibility[subscription_plan.name]:
                if "total_" in header:
                    #if "totals" in pricing_eligibility[subscription_plan.name]:
                    is_eligible = False
                else:
                    is_eligible = True
            if header in pricing_eligibility[subscription_plan.name]:
                is_eligible = True
    return is_eligible


